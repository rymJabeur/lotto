package com.lotto.model;

public class LottoResult {
	private String firstTirage;
	private String secondTirage;
	private String thirdTirage;

	public String getFirstTirage() {
		return firstTirage;
	}

	public void setFirstTirage(String firstTirage) {
		this.firstTirage = firstTirage;
	}

	public String getSecondTirage() {
		return secondTirage;
	}

	public void setSecondTirage(String secondTirage) {
		this.secondTirage = secondTirage;
	}

	public String getThirdTirage() {
		return thirdTirage;
	}

	public void setThirdTirage(String thirdTirage) {
		this.thirdTirage = thirdTirage;
	}

}
